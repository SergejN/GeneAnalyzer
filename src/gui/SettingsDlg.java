private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    if(jcbHeap.isSelected())
    {
        if(jeHeap.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Set the heap space size", 
                                          Main.APPTITLE, JOptionPane.ERROR_MESSAGE);
            return;
        }
        sm.addSetting("", SettingsManager.HEAPSIZE, jeHeap.getText());
    }
    else
        sm.removeSetting("", SettingsManager.HEAPSIZE);
    sm.addSetting("", SettingsManager.CODONTABLE, (String)jcbTables.getSelectedItem());
    if(jrbSimple.isSelected())
        sm.addSetting("", SettingsManager.CODETYPE, SettingsManager.CT_SIMPLE);
    else if(jrbExtended.isSelected())
        sm.addSetting("", SettingsManager.CODETYPE, SettingsManager.CT_EXTENDED);
    else
        sm.addSetting("", SettingsManager.CODETYPE, SettingsManager.CT_COMPLETE);
    sm.addSetting("", SettingsManager.LOCALE, (jcbLocale.getSelectedIndex()==0) ? "Germany" : "US");    
    setVisible(false);
}//GEN-LAST:event_jButton1ActionPerformed

