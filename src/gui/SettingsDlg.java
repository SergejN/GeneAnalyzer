    private SettingsManager sm = null;

    public SettingsDlg(Kernel kernel) 
    {
        super((java.awt.Frame)null, true);
        initComponents();
        setLocationRelativeTo(null);
        sm = kernel.getInitializationData().sm;
        String[] ct = kernel.listCodonTables();
        int iIndex = -1;
        String strCT = sm.getSetting("", SettingsManager.CODONTABLE);
        for(int i=0;i<ct.length;i++)
        {
            jcbTables.addItem(ct[i]);
            if(strCT!=null && ct[i].equals(strCT))
                iIndex = i;
        }        
        if(iIndex>-1)
            jcbTables.setSelectedIndex(iIndex);
        // Code.
        String strCode = sm.getSetting("", SettingsManager.CODETYPE);
        if(strCode==null || strCode.equalsIgnoreCase(SettingsManager.CT_SIMPLE))
            jrbSimple.setSelected(true);
        else if(strCode.equalsIgnoreCase(SettingsManager.CT_EXTENDED))
            jrbExtended.setSelected(true);
        else
            jrbComplete.setSelected(true);
        // Locale.
        String strLocale = sm.getSetting("", SettingsManager.LOCALE);
        if(strLocale==null || strLocale.equalsIgnoreCase("Germany"))
            jcbLocale.setSelectedIndex(0);
        else
            jcbLocale.setSelectedIndex(1);
        // Heap space.
        String strHeap = sm.getSetting("", SettingsManager.HEAPSIZE);
        if(strHeap!=null && strHeap.matches("[0-9]+"))
        {
            jcbHeap.setSelected(true);
            jeHeap.setText(strHeap);
            jeHeap.setEnabled(true);
        }
    }

