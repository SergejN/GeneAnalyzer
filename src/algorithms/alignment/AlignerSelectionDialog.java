    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int i = jlAligners.getSelectedIndex();
        if(i==-1)
        {
            JOptionPane.showMessageDialog(null, "Please, select an alignment algorithm", Main.APPTITLE, JOptionPane.ERROR_MESSAGE);
            return;
        }
        else
            index = i;
        setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jlAlignersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlAlignersMouseClicked
        if(evt.getClickCount()==2)
        {
            int i = jlAligners.getSelectedIndex();
            if(i==-1)
                return;
            index = i;
            setVisible(false);
        }
    }//GEN-LAST:event_jlAlignersMouseClicked

