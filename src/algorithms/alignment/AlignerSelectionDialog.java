    public Options getOptions(IAligner[] aligners)
    {
        DefaultListModel model = new DefaultListModel();
        for(IAligner al:aligners)
            model.addElement(al.getName());
        jlAligners.setModel(model);
        setVisible(true);
        if(index==-1)
            return null;
        else
        {
            Options opt = new Options();
            opt.aligner = aligners[index];
            opt.bUseSim = jcbSimilarNamesOnly.isSelected();
            return opt;
        }
    }

